class Solution{
public:
    int remove_duplicate(int arr[],int n){
        set<int> st;
        for(int i = 0; i < n; i++) {
            st.insert(arr[i]);
        }
        int index = 0;
        for(auto it : st ) {
            arr[index]  = it;
            index++;
        }
        return index;
    }
};

